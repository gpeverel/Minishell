#include "libft.h"

char	*ft_strrchr(const char *str, int ch)
{
	int len;

	len = ft_strlen(str);
	while (len != -1)
	{
		if (str[len] == ch)
			return (((char *)str) + len);
		len--;
	}
	return (NULL);
}

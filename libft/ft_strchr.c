#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	if (!*str)
	{
		if (*str == ch)
			return ((char *)str);
		return (NULL);
	}
	while (*str)
	{
		if (*str == ch)
			return ((char *)str);
		str++;
		if (*str == ch)
			return ((char *)str);
	}
	return (NULL);
}

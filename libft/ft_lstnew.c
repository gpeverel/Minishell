#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*str;

	if (!(str = (t_list *)malloc(sizeof(t_list))))
		return (NULL);
	str->content = content;
	str->next = NULL;
	return (str);
}
